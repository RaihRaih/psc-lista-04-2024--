public class Numeros {

    public static void main(String[] args) {
        // Imprime os números de 1 a 20 abaixo um do outro
        System.out.println("Números de 1 a 20 abaixo um do outro:");
        for (int i = 1; i <= 20; i++) {
            System.out.println(i);
        }

        // Imprime os números de 1 a 20 um ao lado do outro
        System.out.println("\nNúmeros de 1 a 20 um ao lado do outro:");
        for (int i = 1; i <= 20; i++) {
            System.out.print(i + " ");
        }
    }
}
